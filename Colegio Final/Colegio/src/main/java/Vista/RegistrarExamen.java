    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
       try {
           String curso = tfnombreCurso.getText();
           String profesor = tfNombreProfesor.getText();
           SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");//el formato de dd/MM/yyyy
           Date fechaExamen = sdf.parse(tfFechaExamen.getText());
           String aulaExamen= tfAulaExamen.getText();
           int  duracion= Integer.parseInt(tfDuracionExamen.getText());
           Examen ex = new Examen(profesor, curso,fechaExamen,aulaExamen, duracion);
           hashTable.insert(ex);
           addRow(ex);
           JOptionPane.showMessageDialog(null,"Examen Registrado Correctamente.");
           clearFields();       // Limpia las cajas de texto
       } catch (ParseException ex) {
           JOptionPane.showMessageDialog(null,"Ocurrió un error. Verifique sus datos.");
       }
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

    public void addRow(Examen ex){
        DefaultTableModel model = (DefaultTableModel) table.getModel(); //obtiene el modelo
        //agrega la fila
        model.addRow(new Object[]{ex.getNombreCurso(),ex.getNombreProfesor(),tfFechaExamen.getText(),ex.getUbicacionExamen(),
        ex.getDuracionExamen()});
    }
    
    public void clearFields(){
        tfnombreCurso.setText("");
        tfNombreProfesor.setText("");
        tfFechaExamen.setText("");
        tfAulaExamen.setText("");
        tfDuracionExamen.setText("");
    }
    
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       try {
           int index = table.getSelectedRow();
           if (index<0){
               JOptionPane.showMessageDialog(null,"Seleccione una fila.");
               return;
           }
           String curso =table.getModel().getValueAt(index, 0).toString();
           String profesor =table.getModel().getValueAt(index,1).toString();
           SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");//el formato de dd/MM/yyyy
           Date fechaExamen = sdf.parse(table.getModel().getValueAt(index, 2).toString());
           String aula =table.getModel().getValueAt(index, 3).toString();
           Integer duracion =Integer.parseInt(table.getModel().getValueAt(index, 4).toString());
          ((DefaultTableModel)table.getModel()).removeRow(index);
           hashTable.remove(new Examen(profesor, curso,fechaExamen, aula, duracion));
       } catch (ParseException ex) {
           Logger.getLogger(RegistrarExamen.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_btnEliminarActionPerformed


